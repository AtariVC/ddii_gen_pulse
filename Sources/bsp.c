#include "stm32f405xx.h"



void BSP_Init() {
  RCC->AHB1ENR |= 0x1FF;  //GPIOA..GPIOI clock enable
  GPIOA->AFR[1] |= 0x00000770;
  GPIOA->MODER = GPIOA->MODER & 0x3C000000;
  GPIOA->MODER |= (2<<10*2) | (2<<9*2) | (3<<5*2) | (3<<4*2);
  GPIOA->PUPDR = (GPIOA->PUPDR & 0x3C000000) | 0x8A96A0AA;
  GPIOB->MODER = 0x15500000;
  GPIOB->PUPDR = 0x800AAAAA;
  GPIOC->MODER = 0;
  GPIOC->PUPDR = 0xAAAAAAAA;
  GPIOD->MODER = 0;
  GPIOD->PUPDR = 0xAAAAAAAA;
  GPIOE->MODER = 0;
  GPIOE->PUPDR = 0xAAAAAAAA;
  GPIOF->MODER = 0;
  GPIOF->PUPDR = 0xAAAAAAAA;
  GPIOG->MODER = 0;
  GPIOG->PUPDR = 0xAAAAAAAA;
  GPIOH->MODER = 0;
  GPIOH->PUPDR = 0xAAAAAAAA;
  GPIOI->MODER = 0;
  GPIOI->PUPDR = 0xAAAAAAAA;
}


